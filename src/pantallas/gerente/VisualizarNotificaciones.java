    public void llenarTabla() {
        try {
            ResultSet r;
            r = u.seleccionar("fecha,concat( nombre, ' ', apellido ) AS nombreCompleto, advertencia,contenido,asunto",
                    "gestiona a, mensaje b, usuario c,es d",
                    "a.idMensaje = b.idMensaje AND c.user = a.user  and c.user=d.user "
                    + "AND tipoMensaje =2 AND visto =0 and codigoTipoUser=1 AND asunto like 'Error Encontrado%'");
            if (r.last()) {
                r.first();
                DefaultTableModel modelo = (DefaultTableModel) noVisualizadas.getModel();
                int i;
                this.mensajes = new ArrayList();
                r.beforeFirst();
                while (r.next()) {
                    Object[] fila = new Object[3];
                    Mensaje m = new Mensaje();
                    for (i = 0; i < 5; i++) {
                        switch (i) {
                            case 0:
                                m.setFecha(r.getString("fecha"));
                                fila[i] = r.getObject(i + 1);
                                break;
                            case 1:
                                fila[i] = r.getObject(i + 1);
                                break;
                            case 2:
                                if (Integer.parseInt(r.getString("advertencia")) == 1) {
                                    fila[i] = "SI";
                                } else {
                                    fila[i] = "NO";
                                }
                                break;
                            case 3:
                                m.setContenido(r.getString("contenido"));
                                break;
                            case 4:
                                m.setAsunto(r.getString("asunto"));
                                break;
                        }
                    }
                    modelo.addRow(fila);
                    mensajes.add(m);
                }
            }
            u.getSentencia().close();
        } catch (SQLException ex) {
            System.out.println(ex);
        }
    }
