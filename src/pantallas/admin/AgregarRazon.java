    private void agregarRazonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarRazonActionPerformed
        Razon r = new Razon();
        r.setA(areas.get(cbAreas.getSelectedIndex() - 1));
        r.setE(especificaciones.get(cbEspecificaciones.getSelectedIndex() - 1));
        if (r.comprobarRazon()) {
            r.generarCodigoRazon();
            r.setNombreRazon(r.getA().getNombreArea()+" "+r.getE().getNombreEspecificacion());
            r.agregarRazon();
            JOptionPane.showMessageDialog(null, "RazÃ³n de envio agregada exitosamente", "Agregar RazÃ³n de envio", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "No se pudo agregar la razÃ³n de envio porque ya existe en el sistema", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_agregarRazonActionPerformed

