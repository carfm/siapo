    private void eliminarMensajes(JTable tabla, String delType) {
        String aux;
        int confirm; //Aprobacion para eliminar mensaje o mensajes.
        switch (delType) {
            case "todo":
                confirm = JOptionPane.showConfirmDialog(null, "Â¿Esta seguro de vaciar la bandeja de mensajes?", "Eliminar mensaje.", JOptionPane.WARNING_MESSAGE);
                if (confirm == JOptionPane.YES_OPTION) {
                    gm.deleteAll();
                    this.ed_textarea1.setText("");
                } else {
                    new Bandeja(u, tipoBand).setVisible(true);
                }
                break;
            case "uno":
                aux = tabla.getValueAt(tabla.getSelectedRow(), 1).toString().trim();
                confirm = JOptionPane.showConfirmDialog(null, "Â¿Esta seguro de eliminar este mensaje?", "Eliminar mensaje.", JOptionPane.WARNING_MESSAGE);
                if (confirm == JOptionPane.YES_OPTION) {
                    gm.deleteOne(aux);
                    this.ed_textarea1.setText("");
                } else {
                    new Bandeja(u, tipoBand).setVisible(true);
                }
                break;
            case "ocultartodo":
                confirm = JOptionPane.showConfirmDialog(null, "Â¿Esta seguro de vaciar la bandeja de mensajes?", "Eliminar mensaje.", JOptionPane.WARNING_MESSAGE);
                if (confirm == JOptionPane.YES_OPTION) {
                    gm.ocultarAll(user);
                    this.ed_textarea1.setText("");
                } else {
                    new Bandeja(u, tipoBand).setVisible(true);
                }
                break;
            case "ocultaruno":
                aux = tabla.getValueAt(tabla.getSelectedRow(), 1).toString().trim();
                confirm = JOptionPane.showConfirmDialog(null, "Â¿Esta seguro de eliminar este mensaje?", "Eliminar mensaje.", JOptionPane.WARNING_MESSAGE);
                if (confirm == JOptionPane.YES_OPTION) {
                    gm.ocultarOne(aux, user);
                    this.ed_textarea1.setText("");
                } else {
                    new Bandeja(u, tipoBand).setVisible(true);
                }
                break;
            default:
                break;
        }

    }

